CREATE TABLE `user` (
    `id` BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    `role` SMALLINT UNSIGNED NOT NULL DEFAULT 0,
    `number` VARCHAR(20) NOT NULL COMMENT '学号/职工号，限20位',
    `name` VARCHAR(20) NOT NULL COMMENT '真实姓名',
    `password` VARCHAR(200) NOT NULL COMMENT '密码',
    `avatar` VARCHAR(200) NOT NULL,
    `gender` TINYINT NOT NULL DEFAULT 0,
    `college` VARCHAR(50) NOT NULL DEFAULT '',
    `grade` SMALLINT UNSIGNED NOT NULL,
    `major` VARCHAR(50) NOT NULL DEFAULT '',
    `organization` VARCHAR(100) NOT NULL DEFAULT '',
    `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (`id`),
    UNIQUE KEY `uidx_number` (`number`),
) ENGINE = InnoDB DEFAULT CHARSET = utf8mb4 COLLATE = utf8mb4_0900_ai_ci;